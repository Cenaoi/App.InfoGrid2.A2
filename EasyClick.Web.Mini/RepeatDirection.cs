using System;
using System.Collections.Generic;
using System.Text;

namespace EasyClick.Web.Mini
{

    // 摘要:
    //     指定列表控件项的显示方向。
    public enum RepeatDirection
    {

        /// <summary>
        /// 列表项以行的形式水平显示，从左到右、自上而下地加载，直到呈现出所有的项。
        /// </summary>
        Horizontal = 0,

        /// <summary>
        /// 列表项以列的形式垂直显示，自上而下、从左到右地加载，直到呈现出所有的项。
        /// </summary>
        Vertical = 1,
    }
}
